export function icons() {
  const $iconList = document.createElement("div");
  $iconList.setAttribute("id", "storybook_icon_list");

  iconList.forEach((icon) => {
    const $icon = document.createElement("img");
    $icon.setAttribute("src", icon.filename);
    $icon.setAttribute("alt", icon.name);
    $icon.setAttribute("title", icon.name);

    const $iconLink = document.createElement("a");
    $iconLink.setAttribute("href", icon.filename);
    $iconLink.setAttribute("download", "");
    $iconLink.appendChild($icon);

    $iconList.appendChild($iconLink);
  });

  return $iconList;
}
